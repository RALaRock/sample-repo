# working with for loops and pizzas

pizzas = ['pepperoni', 'sausage', 'cheese', 'mushroom', 'three cheese']

# print out pizza
for pizza in pizzas:
    print(pizza)

# print out as sentence about each pizza
for pizza in pizzas:
    print(f'The best pizza is anything but {pizza} pizza!')

print('\nI LOVE pizza!!!')
